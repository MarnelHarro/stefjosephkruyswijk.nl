
                <h3>Over Framework</h3>

                Framework is een hulpmiddel om snel een website op te zetten. 

                <strong>Technieken</strong>
                Voor <strong>Framework</strong> wordt PHP als voornaamste techniek gebruikt. Voor het opzetten van de webpagina's wordt HTML gebruikt.
                <p>Met PHP worden de webpagina's steeds opgebouwd. Voor elk onderdeel van een webpagina is een sjabloon gemaakt dat steeds wordt geladen. Hierdoor is onderhoud van de website gecentraliceerd en makkelijker uit te voeren. </p>
                <p>Voor het bewerken van de inhoud van de webpagina's zijn meerdere technieken mogelijk. Deze bewerking vind plaats in een PHP-bestand waardoor het makkelijk is technieken als HTML, PHP of JavaScript te gebruiken.</p>

                