                <h3 id="framework">Framework</h3>
                <strong>Framework</strong> is een sjabloon voor het snel opzetten van een website. Een verzameling van bestanden zorgt dat de website direct actief kan zijn. Door het aanpassen van instellingen, is de opmaak en inhoud van de webpagina's aan te passen.

