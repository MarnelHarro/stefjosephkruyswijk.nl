        <p>Een priemgetal is een natuurlijk getal groter dan 1, met precies twee delers. Het getal 21 heeft als delers 1, 3, 7 en 21. Met vier delers is het geen priemgetal. Het getal 23 heeft als delers 1 en 23. Met twee delers is het wel een priemgetal.
        <br />Het getal 2 is de eerste priemgetal, met als delers 1 en 2. Omdat 2 een even getal is, zijn alle overige even getallen hierdoor geen priemgetallen. Alle even getallen zijn deelbaar door 2.</p> 

